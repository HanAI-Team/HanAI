async def seed():
    db_url = settings.DATABASE_URL.replace("postgresql://", "postgresql+asyncpg://", 1)
    engine = create_async_engine(db_url, echo=False, connect_args={"statement_cache_size": 0})
    async_session = sessionmaker(engine, class_=AsyncSession, expire_on_commit=False)

    async with async_session() as session:
        before = (await session.execute(text("SELECT COUNT(*) FROM claim_rejection_codes"))).scalar()

        # detail_code=None(세부코드 없는 상위 코드)을 ""로 통일 — Postgres는 NULL을
        # 유니크 제약에서 서로 다른 값으로 취급해 ON CONFLICT가 매칭되지 않기 때문.
        rows = [
            {"category": category, "code": code, "detail_code": detail_code or "", "description": description}
            for category, code, detail_code, description in _ROWS
        ]
        stmt = pg_insert(ClaimRejectionCode).values(rows)
        stmt = stmt.on_conflict_do_update(
            index_elements=["category", "code", "detail_code"],
            set_={"description": stmt.excluded.description},
        )
        result = await session.execute(stmt)
        await session.commit()

        after = (await session.execute(text("SELECT COUNT(*) FROM claim_rejection_codes"))).scalar()

    await engine.dispose()
    print(f"기존 {before}개 → 삽입/갱신 처리 {result.rowcount}건 → 전체 {after}개")


if __name__ == "__main__":
    asyncio.run(seed())
