async def seed():
    async with AsyncSessionLocal() as session:
        result = await session.execute(text("SELECT COUNT(*) FROM kcd_u_codes"))
        count = result.scalar()
        if count > 0:
            print(f"이미 {count}개 데이터 존재. 스킵.")
            return

        rows = [{"code": code, "korean_name": name} for code, name in KCD_U_CODES]
        await session.execute(
            text("INSERT INTO kcd_u_codes (code, korean_name) VALUES (:code, :korean_name)"),
            rows,
        )
        await session.commit()
        print(f"{len(rows)}개 KCD U코드 시딩 완료.")


if __name__ == "__main__":
    asyncio.run(seed())